    public void refrescarList(){
        mostrarTitulo.setText(pl.getNombrePlayList());
        modelo.removeAllElements(); 
        for(Cancion can : pl.getListaCanciones()){
            modelo.addElement(can.getNombre());
        }
    }
    private void listaCancionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaCancionesMouseClicked
        String nombre = listaCanciones.getSelectedValue();
        for(Cancion cn : pl.getListaCanciones()){
            if(cn.getNombre().equals(nombre)){
                cancion = cn;
                continue;
            }
        }
        setnombreCancion.setText(cancion.getNombre());
        nombreArtista.setText(cancion.getAutor());
        duracion.setText(cancion.getDuracion());
    }//GEN-LAST:event_listaCancionesMouseClicked

